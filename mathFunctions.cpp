#include<iostream>
#include<cmath> // using math package
using namespace std;

int main(){
cout<< abs(-12.5) <<endl; // returns the absolute value
cout<< acos(0.8) <<endl;  // returns the arc cosine value
cout<< asin(0.8) <<endl;  // returns the arc sine value
cout<< atan(0.8)  <<endl;  // retuens the arc tan value
cout<< sin(60)   <<endl;  // returns the sine value
cout<< cos(60)   <<endl;  // returns the cosine value
cout<< tan(60)   <<endl;  // returns the tangent value
cout<< max(22,24) <<endl; // returns the maximum value
cout<< min(22,24) <<endl; // returns the minimum value
cout<< cbrt(64)   <<endl; // returns the cube root value
cout<< ceil(2.87) <<endl; // returns the round up value
cout<< sinh(60)   <<endl; // returns the sine hyper bolic value
cout<< cosh(60)   <<endl; // returns the cos hyper bolic value
cout<< tanh(60)   <<endl; // returns the tan hyperbolic value
cout<< exp(56)    <<endl; // returns the exponential value(E^x)
cout<< expm1(56)  <<endl; // returns e^x-1
cout<< pow(2,4)   <<endl; // returns power (a^b)
return 0;
}
